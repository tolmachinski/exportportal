function translate_js(names){
    var plug = names.plug;
    var text = names.text;
    var lang = Object.assign({}, window[plug]);

    if(lang == undefined || lang == ""){
        lang = window[plug];
    }

    if(typeof names.replaces !== 'undefined'){
        for (let prop in names.replaces) {
            lang[text] = lang[text].replace(prop, names.replaces[prop]);
        }
    }

    return lang[text];
}

